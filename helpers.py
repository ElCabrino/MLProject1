def dictionarify(w):
    return { f'w{i[0]}': w for i, w in np.ndenumerate(w) }


def find_arg_min(res, value):
    val = np.vectorize(lambda x: x[value])(res)
    index = int(np.where(val == val.min())[0][0])
    return res[index]
